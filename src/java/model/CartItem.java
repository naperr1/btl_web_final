package model;

/**
 *
 * @author lap
 */
public class CartItem {

    private String id;
    private Item item;
    private User user;
    private int quantity;

    public CartItem() {
    }

    public CartItem(String id, Item item, User user, int quantity) {
        this.id = id;
        this.item = item;
        this.user = user;
        this.quantity = quantity;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Item getItem() {
        return item;
    }

    public void setItem(Item item) {
        this.item = item;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

}
